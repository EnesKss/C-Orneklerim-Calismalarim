#include <stdio.h>

void bubbleSort(int arr[], int n) {
    int i, j;
    for (i = 0; i < n-1; i++) {
        // Her geçitte en büyük elemaný saða doðru kaydýrmak için iç içe döngü
        for (j = 0; j < n-i-1; j++) {
            // Yan yana iki elemaný karþýlaþtýr ve gerektiðinde yerlerini deðiþtir
            if (arr[j] > arr[j+1]) {
                int temp = arr[j];
                arr[j] = arr[j+1];
                arr[j+1] = temp;
            }
        }
    }
}

int main() {
    int n;
    printf("Dizinin boyutunu girin: ");
    scanf("%d", &n);
    
    int arr[n];
    printf("Dizinin elemanlarýný girin:\n");
    for (int i = 0; i < n; i++) {
        scanf("%d", &arr[i]);
    }
    
    printf("Dizinin sýrasýz hali: \n");
    for (int i = 0; i < n; i++) {
        printf("%d ", arr[i]);
        
    }
    printf("\n");
    
    bubbleSort(arr, n);
    
    printf("Dizinin sýralý hali: \n");
    for (int i = 0; i < n; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");
    
    return 0;
}

